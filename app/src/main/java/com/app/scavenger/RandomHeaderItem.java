package com.app.scavenger;

public class RandomHeaderItem {

    RandomHeaderItem() {}

    private String headerText;

    private String numRecipesText;

    public String getHeaderText() {
        return headerText;
    }

    public void setHeaderText(String headerText) {
        this.headerText = headerText;
    }

    public String getNumRecipesText() {
        return numRecipesText;
    }

    public void setNumRecipesText(String numRecipesText) {
        this.numRecipesText = numRecipesText;
    }
}
